// 獲取元素
const openexplButton1 = document.getElementById("openexpl1");
const closeexplButton1 = document.getElementById("closeexpl1");
const expl1 = document.getElementById("expl1");

// 打開彈出窗口
openexplButton1.addEventListener("click", function() {
  expl1.style.display = "block";
});

// 關閉彈出窗口
closeexplButton1.addEventListener("click", function() {
  expl1.style.display = "none";
});

